void displayMenu() {
  display.clearDisplay();
  display.setTextSize(2);
  display.setTextColor(WHITE);

  for (int i = 0; i < display_items; i++) {
    int idx = menu_start + i;
    if (idx < menu_items) {
      int x = 10;
      int y = i * 20;

      String menuText = isMenuSwitched ? switched_menu[idx] : main_menu[idx];

      if (idx == menu_index) {
        display.drawRect(x - 5, y - 2, 100, 20, WHITE);  // วาดกรอบสี่เหลี่ยม
      }

      display.setCursor(x, y);
      display.println(menuText);
    }
  }
  display.display();
}

void displayValue(String title, float value, String unit, const uint8_t *icon) {
  display.clearDisplay();
  display.setTextSize(2);
  display.setTextColor(WHITE);

  // แสดงไอคอนที่ตำแหน่ง (10, 15)
  display.drawBitmap(-50, 13, icon, 128, 64, WHITE);

  // คำนวณตำแหน่งข้อความ
  int16_t x1, y1;
  uint16_t w, h;
  
  display.getTextBounds(title, 0, 0, &x1, &y1, &w, &h);
  int titleX = 40;  // ขยับข้อความไปทางขวา
  int titleY = 5;   // ขยับขึ้นเล็กน้อย

  // ข้อความที่จะแสดง 
  String valueText = String(value) + " " + unit;
  display.getTextBounds(valueText, 0, 0, &x1, &y1, &w, &h);
  int valueX = 40;   // ขยับข้อความไปทางขวา
  int valueY = 40;

  // แสดงข้อความที่ขวาของไอคอน
  display.setCursor(titleX, titleY);
  display.println(title);
  display.setCursor(valueX, valueY);
  display.println(valueText);
  display.display();
  
  // รอให้กดปุ่ม BACK ก่อนออก
  while (digitalRead(BTN_BACK) == HIGH);
  delay(200);
  mode = SWITCHED_MENU;
  displayMenu();
}

void reconnect() {
  while (!client.connected()) {
    Serial.print("Attempting MQTT connection...");
    
    if (client.connect("ESP32Client", access_token, "")) {
      Serial.println("connected");
      client.subscribe("v1/devices/me/rpc/request/+");
    } else {
      Serial.print("failed, rc=");
      Serial.print(client.state());
      Serial.println(" try again in 5 seconds");
      delay(5000);
    }
  }
}


