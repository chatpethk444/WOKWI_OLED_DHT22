// ฟังก์ชัน callback ที่แก้ไขแล้ว
void callback(char* topic, byte* payload, unsigned int length) {
  // แปลง payload เป็น string
  char message[length + 1];
  memcpy(message, payload, length);
  message[length] = '\0';
  
  Serial.print("Received [");
  Serial.print(topic);
  Serial.print("]: ");
  Serial.println(message);

  // แปลง JSON
  JsonDocument doc;
  DeserializationError error = deserializeJson(doc, message);
  
  if (error) {
    Serial.print("JSON parse error: ");
    Serial.println(error.c_str());
    return;
  }

  // ตรวจสอบ method โดยตัดช่องว่างออก
  String method = doc["method"].as<String>();
  method.trim();
  
  if (method != "led") {
    Serial.println("Not a LED command");
    return;
  }

  // ตรวจสอบ params ในรูปแบบต่างๆ
  bool ledState = false;
  
  if (doc["params"].is<bool>()) {
    ledState = doc["params"];
  } 
  else if (doc["params"].is<const char*>()) {
    const char* param = doc["params"];
    if (strcasecmp(param, "ON") == 0 || strcasecmp(param, "TRUE") == 0 || strcmp(param, "1") == 0) {
      ledState = true;
    }
  }
  else if (doc["params"].isNull()) {
    ledState = !digitalRead(ledpin);
  }

  // ควบคุม LED
  digitalWrite(ledpin, ledState ? HIGH : LOW);
  Serial.print("LED set to: ");
  Serial.println(ledState ? "ON" : "OFF");  // แก้ไขจาก ledpin เป็น ledState

  // ส่ง response กลับ
  char response[50];
  snprintf(response, sizeof(response), "{\"led\":%s}", ledState ? "true" : "false");
  client.publish("v1/devices/me/attributes", response);

  // ตอบกลับ RPC request
  if (doc["id"]) {
    char rpcResponse[100];
    snprintf(rpcResponse, sizeof(rpcResponse), 
             "{\"method\":\"led\",\"params\":%s,\"id\":\"%s\"}", 
             ledState ? "true" : "false", 
             doc["id"].as<const char*>());
             
    String responseTopic(topic);
    responseTopic.replace("request", "response");
    client.publish(responseTopic.c_str(), rpcResponse);
  }
}




