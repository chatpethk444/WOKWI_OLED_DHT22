void loop() {
	float temp = dht.readTemperature();
	float humid = dht.readHumidity();


  if (mode == MAIN_MENU || mode == SWITCHED_MENU) {
    if (digitalRead(BTN_UP) == LOW) {
      if (menu_index > 0) {
        menu_index--; 
        if (menu_index < menu_start) {
          menu_start = menu_index;
        }
        displayMenu();
      }
      delay(200);
    }
    if (digitalRead(BTN_DOWN) == LOW) {
      if (menu_index < menu_items - 1) {
        menu_index++; 
        if (menu_index >= menu_start + display_items) {
          menu_start = menu_index - display_items + 1;
        }
        displayMenu();
      }
      delay(200);
    }
    if (digitalRead(BTN_SELECT) == LOW) {
      if (mode == MAIN_MENU && (menu_index == 1 || menu_index == 2)) {
        isMenuSwitched = true;  
        mode = SWITCHED_MENU;
      } else if (mode == SWITCHED_MENU) {
        if (menu_index == 1) {  // Temp
          displayValue("Temp", dht.readTemperature(), "C", temp_icon);
        } else if (menu_index == 2) {  // Humid
          displayValue("Humid", dht.readHumidity(), "%", humid_icon);
        }
      }
      displayMenu();
      delay(500);
    }
    if (digitalRead(BTN_BACK) == LOW) {
      isMenuSwitched = false;
      mode = MAIN_MENU;
      displayMenu();
      delay(200);
    }
  }


  	if (!client.connected()) {
	reconnect();
	}
  client.loop();

  static unsigned long lastSend = 0;
  if (millis() - lastSend > 5000) {
    String payload = "{\"temperature\":" + String(temp, 2) + ",\"humidity\":" + String(humid, 2) + "}";
    if (client.publish("v1/devices/me/telemetry", payload.c_str())) {
      Serial.println("Data sent to ThingsBoard");
    } else {
      Serial.println("Failed to send data");
    }
    lastSend = millis();
  }


}