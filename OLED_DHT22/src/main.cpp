void displayMenu() {
  display.clearDisplay();
  display.setTextSize(2);
  display.setTextColor(WHITE);

  for (int i = 0; i < display_items; i++) {
    int idx = menu_start + i;
    if (idx < menu_items) {
      int x = 10;
      int y = i * 20;

      String menuText = isMenuSwitched ? switched_menu[idx] : main_menu[idx];

      if (idx == menu_index) {
        display.drawRect(x - 5, y - 2, 100, 20, WHITE);  // วาดกรอบสี่เหลี่ยม
      }

      display.setCursor(x, y);
      display.println(menuText);
    }
  }
  display.display();
}

void displayValue(String title, float value, String unit, const uint8_t *icon) {
  display.clearDisplay();
  display.setTextSize(2);
  display.setTextColor(WHITE);

  // แสดงไอคอนที่ตำแหน่ง (10, 15)
  display.drawBitmap(-50, 13, icon, 128, 64, WHITE);

  // คำนวณตำแหน่งข้อความ
  int16_t x1, y1;
  uint16_t w, h;
  
  display.getTextBounds(title, 0, 0, &x1, &y1, &w, &h);
  int titleX = 40;  // ขยับข้อความไปทางขวา
  int titleY = 5;   // ขยับขึ้นเล็กน้อย

  // ข้อความที่จะแสดง 
  String valueText = String(value) + " " + unit;
  display.getTextBounds(valueText, 0, 0, &x1, &y1, &w, &h);
  int valueX = 40;   // ขยับข้อความไปทางขวา
  int valueY = 40;

  // แสดงข้อความที่ขวาของไอคอน
  display.setCursor(titleX, titleY);
  display.println(title);
  display.setCursor(valueX, valueY);
  display.println(valueText);
  display.display();
  
  // รอให้กดปุ่ม BACK ก่อนออก
  while (digitalRead(BTN_BACK) == HIGH);
  delay(200);
  mode = SWITCHED_MENU;
  displayMenu();
}


void setup() {
  pinMode(BTN_UP, INPUT_PULLUP);
  pinMode(BTN_DOWN, INPUT_PULLUP);
  pinMode(BTN_SELECT, INPUT_PULLUP);
  pinMode(BTN_BACK, INPUT_PULLUP);
  
  dht.begin();
  display.begin(SSD1306_SWITCHCAPVCC, 0x3C);
  displayMenu();
}

void loop() {
  if (mode == MAIN_MENU || mode == SWITCHED_MENU) {
    if (digitalRead(BTN_UP) == LOW) {
      if (menu_index > 0) {
        menu_index--; 
        if (menu_index < menu_start) {
          menu_start = menu_index;
        }
        displayMenu();
      }
      delay(200);
    }
    if (digitalRead(BTN_DOWN) == LOW) {
      if (menu_index < menu_items - 1) {
        menu_index++; 
        if (menu_index >= menu_start + display_items) {
          menu_start = menu_index - display_items + 1;
        }
        displayMenu();
      }
      delay(200);
    }
    if (digitalRead(BTN_SELECT) == LOW) {
      if (mode == MAIN_MENU && (menu_index == 1 || menu_index == 2)) {
        isMenuSwitched = true;  
        mode = SWITCHED_MENU;
      } else if (mode == SWITCHED_MENU) {
        if (menu_index == 1) {  // Temp
          displayValue("Temp", dht.readTemperature(), "C", temp_icon);
        } else if (menu_index == 2) {  // Humid
          displayValue("Humid", dht.readHumidity(), "%", humid_icon);
        }
      }
      displayMenu();
      delay(500);
    }
    if (digitalRead(BTN_BACK) == LOW) {
      isMenuSwitched = false;
      mode = MAIN_MENU;
      displayMenu();
      delay(200);
    }
  }
}
